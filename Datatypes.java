public class Datatypes {
    public static void main(String args[])
    {
        Boolean a = true;
        Boolean b = false;
        int i = 678; // stores integers from -2,147,483,648 to 2,147,483,647 
        char c = 'B';
        // byte b = 4;  // stores whole numbers from  -128 to 127
        short s = 67;
        double d = 4.578694748; // stores upto 16 digits after the decimal
        float f = 3.14f; // stores upto 7 digits after the decimal.
        System.out.println("boolean:"+a);
        System.out.println("boolean1:"+b);
        System.out.println("int:"+i);
        System.out.println("char:"+c);
        System.out.println("byte:"+b);
        System.out.println("short:"+s);
        System.out.println("double:"+d);
        System.out.println("float:"+f);
        System.out.println(Integer.MIN_VALUE);
        System.out.println(Integer.MAX_VALUE);
        System.out.println(Integer.SIZE);
        System.out.println(Short.MIN_VALUE);
        System.out.println(Short.MAX_VALUE);





    // output:
    // boolean:true
    // boolean1:false
    // int:678
    // char:B
    // short:67
    // double:4.578694748
    // float:3.14
        
        





    }
}
